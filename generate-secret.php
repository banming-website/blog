<?php
/**
 * 生成随机密钥
 * 用于 JWT_SECRET 等配置
 */

// 生成 64 字符的随机密钥
$secret = bin2hex(random_bytes(32));

echo "============================================\n";
echo "   随机密钥生成器\n";
echo "============================================\n\n";

echo "生成的密钥：\n";
echo $secret . "\n\n";

echo "使用方法：\n";
echo "将以下代码复制到 config.php 中：\n\n";

echo "define('JWT_SECRET', '{$secret}');\n\n";

echo "============================================\n";
echo "注意：请妥善保管此密钥，不要泄露！\n";
echo "============================================\n";
?>
